        <b>« European Association of Economics Practitioners»</b>.</li>
        <p class="font_bold">§2<br>Siège social</p>
        <p class="font_regular">Son siège social est établi au 6 Rond-Point Schuman, Box 5, à 1040 Bruxelles,
            dans l’arrondissement judiciaire de Bruxelles. Il peut être transférer par simple décision en 
            l’assemblée générale (AG), dans tout autre lieu del’arrondissement judiciaire de Bruxelles.
        </p>
        <p class="font_bold">§3<br>But social</p>
        <p class="font_regular">L'association a pour but :<br>
        <ol class="list0 nls-None font_regular">
            <li>Le soutien au développement de l'entrepreneuriat en Belgique au travers d’une coopération
                et d'intégration avec les pays européens,</li>
            <li>Le soutien des entrepreneurs européens sur les marchés asiatique, américains, australiens 
                et africains,</li>
            <li>L’amélioration d’une communication et des contacts entre les théoriciens et les praticiens
                de l'économie en Europe,</li>
            <li>L’encouragement d’une meilleure compréhension des sciences économiques par le biais d’un
                échange scientifique et culturel entre les pays de la Communauté Européenne,</li>
            <li>La promotion d’une combinaison de l'étude des sciences économiques avec la sagesse dérivée
                de la pratique, en particularité l’encouragement au travail des étudiants,</li>
            <li>la promotion de l'idée de l'apprentissage tout au long de la vie. Soutien aux objectifs de la 
                politique de la Commission Européenne en matière de développement de l’enseignement en Europe.</li>
            <li>L'intégration européenne et le développement des contacts et d’une coopération entre des 
                communautés locales de différents États membres de l'UE,</li>
            <li>La promotion de l'idée d'une société saine.</li>
        </ol>
        <p class="font_regular">L'association réalise ses objectifs aux travers de :</p>
        <ol class="list0 nls-None font_regular">
            <li>Le soutien financier et le partenariat,</li>
            <li>L’échange d’expériences,</li>
            <li>L’organisation des séminaires, des formations et des autres événements à caractère éducatif, 
                culturel, social, scientifique et philanthropique,</li>
            <li>La publication des revues, des périodiques et des livres etc., dans les domaines :
                des différentes activités économiques, scientifiques, culturelles et de tourisme, dans tous 
                les pays de l’Europe,</li>
            <li>La diffusion des revues, des périodiques et des livres, etc., dans tous les pays de 
                l’Europe,</li>
            <li>L’accompagnement dans le champ de l’apprentissage de l’économie en pratique,</li>
            <li>La coopération avec des institutions similaires en Belgique et à l’étranger,</li>
        </ol>
        <p class="font_regular">Elle peut accomplir tous les actes se rapportant indirectement ou
            directement à son objet. Elle peut notamment prêter son concours et s'intéresser à toute 
            activité similaire à son objet.
        </p>
        <p class="font_bold">§4<br>Exercice social</p>
        <p class="font_regular">L'exercice social de l’association début le 1er janvier pour se terminer 
            le 31 décembre. Sans préjudice de l’alinéa  précédent, l’exercice sociale pour l’année 2018 début 
            le jour de la publication des présents statuts au moniteur belge et se termine le 31 décembre 2019.
        </p>
        <p class="font_bold">§5<br>Durée</p>
        <p class="font_regular">L’association est créée pour une durée illimitée. Elle peut être dissoute
            en tout temps conformément à la loi et aux présents statuts.
        </p>
        <p class="font_titre">TITRE II – MEMBRES.</p>
        <p class="font_bold">§6<br>Composition</p>
        <p class="font_regular">L'association est composée de membres effectifs, de membres adhérents et de
            membres d’honneurs. Le nombre minimum des membres effectifs ne peut être inférieur à 3(à trois).
            Seuls les membres effectifs jouissent de la plénitude des droits accordés aux membres par la loi 
            et les présents statuts.
        </p>
        <p class="font_bold">§7<br>Membres effectifs</p>
        <p class="font_regular">Est membre effectif de l’association, toute personne présentée à une assemblée
            générale (ciaprès « AG ») par deux membres effectifs au moins et admise en cette qualité par une
            décision de ladite AD réunissant les deux tiers des voix présentes ou représentées, parce 
            qu’elle est active dans son environnement pour la réalisation des buts de l’association ou 
            exerce une fonction active au sein de l’association. Le quorum pour cette décision
            étant fixé à la moitié des membres effectifs. Les comparants à l’acte de constitution sont les
            membres fondateurs. Les membres fondateurs sont de plein droit membres effectifs. La cotisation
            des membres effectifs est de maximum 1.000.000 euros par ans.
        </p>
        <p class="font_bold">§8<br>Membres adhérents</p>
        <p class="font_regular">Est membre adhérent de l’association, toute personne inscrite au registre 
            des membres en qualité de membre adhérent, en ordre de cotisation, et soutenant l’objet sociale de 
            l’association tel que défini par l’art. 3 des présents statuts. Les droits et obligations des
            membres adhérents sont fixés par les présents statuts.
        </p>
        <p class="font_bold">§9<br>Membres d’honneur</p>
        <p class="font_regular">Est membre d’honneur de l’association, toute personne inscrite au registre 
            des membres en qualité de membre d’honneur, soutenant l’objet sociale de l’association tel que
            défini par l’art. 3 des présents statuts, et ayant contribué financièrement au développement 
            de l’association à concurrence de minimum 25€ et maximum 1.000.000€.
        </p>
        <p class="font_bold">§10<br>Droits de vote attaché à la qualité de membre</p>
        <p class="font_regular">Les membres adhérents et les membres d’honneur peuvent participer aux 
            délibérations de l4AG avec voix consultative. Ils ne peuvent en outre être membre du Conseil 
            d’Administration (ciaprès « AC »).
        </p>
        <p class="font_bold">§11<br>Preuve de la qualité de membre</p>
        <p class="font_regular">La qualité de membre se prouve par inscription au registre des membres,
            conservé au siège sociale et tenu à jour par un des administrateurs. Les participants aux
            activités organisées par l’ASBL ne sont pas de facto considérés comme étant des membres adhérents.
        </p>
        <p class="font_bold">§12<br>Perte de qualité de membre</p>
        <p class="font_regular">La démission ou l’exclusion de tout membre intervient selon les dispositions
            prévues par l’art 12 de la loi sur les associations sans but lucratif du 27 juin 1921. L’Assemblée 
            Générale est la seule à pouvoir prononcer l’exclusion d’un membre en cas de manquements aux 
            obligations résultat des présents statuts ou de la loi. Cette décision est prise à la majorité
            des deux tiers des voix présentes ou représentés.
            <br><br> Est notamment visé par l’alinéa précédent, le fait pour un membre :
        </p>
        <br>
        <ol class="list0 nls-None font_regular">
            <li>d’adopter une attitude contraire ou qui met en couse l’honneur de l’association,</li>
            <li>de s’opposer aux objectifs de l’association,</li>
            <li>de nuire au but social poursuivi par l’association,</li>
            <li>d’abuser de sa qualité de membre à des fins personnelles, en ce compris à des
                finscommerciales.</li>
        </ol>
        <br>
        <p class="font_regular">Est réputé démissionnaire le membre effectif qui n’est ni présent ni
            représenté à deux AG cconsécutives sans s’excuser ou tout membre en retard de plus de 3 mois 
            dans le paiement de sa cotisation.
        </p>
        <br>
        <p class="font_regular">Le membre démissionnaire ou exclu n’a aucun droit sur le fond social 
            et ne peut pas réclamer le remboursementdes cotisations qu’il a versées
        </p>
        <p class="font_bold">§13<br>Suspension de la qualité de membre</p>
        <p class="font_regular">La suspension de la qualité de membre peut être prononcée par le CA 
            en l’attente d’une décision de l’Assemblée Générale concernant l’éventuelle exclusion de celui-ci.
        </p>
        <p class="font_bold">§14<br>Cotisation annelle</p>
        <p class="font_regular">Sans préjudice de l’art 9 des présents statuts, la cotisation annuelle exigée
            à l’égard de tout membre est de minimum 1€ et fixé souverainement par l'assemblée générale, sans
            qu’elle ne puisse dépasser 10.000€, pour les membres effectifs et adhérents.
        </p>
        <p class="font_titre">TITRE II – ASSEMBLEE GENERALE.</p>
        <p class="font_bold">§15<br>Composition</p>
        <p class="font_regular">L'assemblée générale est composée de tous les membres effectifs.
            Les membres adhérents et les membres d’honneurspeuvent y prendre part de manière consultative.
        </p>
        <p class="font_bold">§16<br>Procuration et quorum</p>
        <p class="font_regular">Chaque membre effectif peut se faire représenter à l’AG par procuration 
            ou par un mandataire ayant la qualité de membre effectif. Chaque mandataire peut détenir au 
            maximum deux procurations. Le Conseil d’Administration peut définir la forme de la procuration 
            et exiger que celle-ci soit déposée au moins trois jours avant l4AG auprès d’un des administrateurs
            de l’association.
        </p>
        <p class="font_regular">L’AG peut valablement délibérer si au moins un moitié plus un des membres
            est présente ou représenté. Dans le cas où l’AG ne réunit pas le quorum requis par l’alinéa 
            précédent, l’AG qui la succède et qui se déroule lors du même exercice social, pourra valablement 
            délibérer même si le quorum requis par l’alinéa précédent n’est pas atteint. Il est fait exception 
            aux deux précédents alinéas en cas de modifications des statuts. Ainsi, le quorum requis est élève
            à deux tiers des membres en cas de modification des statuts ou de l’objet social.
        </p>

        <p class="font_bold">§17<br>Compétences</p>
        <p class="font_regular">L'assemblée générale est le pouvoir souverain de l'association. 
            Elle possède les pouvoirs qui lui sont expressémentreconnus par la loi ou les présents statuts.
        </p> <br>
        <p class="font_regular">Sont notamment réservées à sa compétence :</p>
        <ol class="list0 nls-None font_regular">
            <li>les modifications aux statuts sociaux;</li>
            <li>la nomination et la révocation des administrateurs;</li>
            <li>le cas échéant la nomination et la révocation des commissaires et la fixation de
             leur rémunération dans le cas où elle leur est attribuée;</li>
            <li>la décharge à octroyer aux administrateurs;</li>
            <li>l'approbation des budgets et des comptes;</li>
            <li>la dissolution volontaire de l'association;</li>
            <li>les exclusions de membres;</li>
            <li>la transformation de l’association en société à finalité sociale;</li>
            <li>tous les cas où les statuts l’exigent.</li>
        </ol>
        <p class="font_bold">§18<br>Assemblée générale ordinaire et extraordinaire.</p>
        <p class="font_regular">Il doit être tenu au moins une assemblée générale chaque année, dans
            le courant du mois de juin. L'association peut être réunie en assemblée générale extraordinaire
            à tout moment par décision du conseil d'administration.Elle doit être réunie lorsqu’un cinquième 
            des membres effectifs au moins en fait la demande. Chaque réunion se tiendra aux jours, heure et 
            lieu mentionnés dans la convocation. Tous les membres effectifs doivent y être convoqués.
        </p>
        <p class="font_bold">§19<br>Convocation</p>
        <p class="font_regular">L'assemblée générale est convoquée par le conseil d'administration par courrier 
            ou par courrier électronique adressé à chaque membre visé à l’article 15 des statuts au moins 8 jours 
            avant l'assemblée, et signée par un administrateur au nom du conseil d'administration. L'ordre du 
            jour est mentionné dans la convocation. Toute proposition signée par le 1/5 des membres effectifs
            doit être portée à l'ordre du jour. Sauf dans les cas prévus aux articles 8, 12 et 20 de la loi 
            du 27 juin 1921, l'assemblée peut délibérer valablement sur des points qui ne sont pas mentionnés
            à l'ordre du jour.
        </p>
        <p class="font_bold">§20<br>Vote</p>
        <p class="font_regular">Les résolutions sont prises à la majorité simple des voix présentes ou 
            représentées, sauf dans le cas où il en est décidé autrement par la loi ou par les présents statuts.
            En cas de partage des voix, un consensus doit être trouvé. Page 5 sur 6 Il est fait en outre 
            exception aux règles du présent article en cas de vote concernent la modification des statuts ou des
            modifications de l’objet social. Dans ce cas, la majorité requise est de deux tiers des voix
            présentes ou représentées. Il est admis un vote électronique, par exemple par e-mail,
            cependant les décisions exprimées par e-mail doivent être exprimées au plus tard avant l’heure de
            la délibération de l’AG.
         </p>
        <p class="font_bold">§21<br>Procès-verbaux</p>
        <p class="font_regular">Les décisions de l'assemblée générale sont consignées dans des procès-verbaux 
            signés par le président et un administrateur. Ces procès-verbaux sont conservés au siège social 
            où tous les membres effectifs peuvent en prendre connaissance. Les membres effectifs ainsi que 
            les tiers justifiant d'un intérêt, peuvent demander des extraits de ces procès-verbaux,
            signés par le président du conseil d'administration et par un administrateur.
        </p>
        <p class="font_bold">§22<br>Publicité des Assemblée Générales</p>
        <p class="font_regular"> Toute modification aux statuts doit être publiée dans le 6 mois aux 
            annexes au Moniteur Belge. Il en est de même de toute nomination, démission ou révocation d’un 
            administrateur.
        </p>
        <p class="font_titre">TITRE IV – CONSEIL D’ADMINISTRATION.</p>
        <p class="font_bold">§23<br>Composition</p>
        <p class="font_regular">L’association est administrée par le Conseil d’Administration composé 
            conformément à l’art 13 de la loi sur les ASBL, par un membre d’administrateur inférieur au nombre 
            de personnes membres de l’association. Les administrateurs sont désignés parmi les membres effectifs
            par l’AG, pour un terme de 3 ans. Les administrateurs sont rééligibles. Le mandat des administrateurs/
            trices est gratuit. Chaque administrateur est révocable en tout temps par une décision prise par
            l’AG selon une majorité de deux tiers des membres effectifs présents et représentés.
        </p>
        <p class="font_bold">§24
        <br>Organisation interne et compétences</p>
        <p class="font_regular">Le conseil se réunit sur convocation du président ou de deux administrateurs.
            Il ne peut statuer que si la majorité de ses membres est présente ou représentée. Ses décisions sont
            prises à la majorité absolue des votants présents ou représentés, la voix du président ou celle 
            de son remplaçant étant, en cas de partage, prépondérante.
        </p>
        <p class="font_regular">Le conseil d'administration a les pouvoirs les plus étendus pour 
            l'administration et la gestion de l'association. Sont seuls exclus de sa compétence, les actes 
            réservés par la loi ou les présents statuts à celle de l'assemblée générale. Le conseil nomme, 
            soit lui-même, soit par mandataire, tous les agents, employés, et membres du personnel de 
            l'association et les destitue. Il détermine leur occupation et leur traitement. Le conseil peut
            déléguer la gestion journalière de l'association, avec l'usage de la signature afférente à cette
            gestion à une ou plusieurs personnes administrateurs ou non et dont il fixera les pouvoirs et
            éventuellement le salaire ou appointement. Lorsque la gestion journalière est confiée à 
            plusieurs personnes, celles-ci agissent individuellement/conjointement/encollège.
          </p>
        <p class="font_bold">§25<br>Organisation interne et compétences</p>
        <p class="font_regular">Un règlement d'ordre intérieur pourra être présenté par le conseil
            d'administration à l'assemblée générale. Des modifications à ce règlement pourront être 
            apportées par une assemblée générale statuant à la majorité simple des membres effectifs présents
            ou représentés.
        </p>

        <p class="font_bold">§26<br>Règlement d'ordre intérieur</p>
        <p class="font_regular">Les comptes de l’exercice écoulé et le budget de l’exercice suivant
            seront annuellement soumis à l’approbation de l’Assemblée Générale Ordinaire.
        </p>
        <p class="font_titre">TITRE IV – DISSOLUTION ET LIQUIDATION</p>
        <p class="font_bold">§27<br>Dissolution et liquidation</p>
        <p class="font_regular">En cas de dissolution de l'association, l'assemblée générale désignera 
            le ou les liquidateurs, déterminera leurs pouvoirs et indiquera l'affectation à donner à l'actif
            net de l'avoir social. Dans tous les cas de dissolution volontaire ou judiciaire, à quelque moment,
            ou par quelque cause qu'elle se produise, l'actif net de l'association dissoute sera affecté à une
            fin désintéressée.
        </p>
        <p class="font_titre">TITRE IV – DISPOSITION DIVERS</p>
        <p class="font_bold">§28<br>Nullité</p>
        <p class="font_regular">Au cas où une des dispositions des présents statuts s’avérait être en
            contradiction avec le droit belge, cette disposition doit être considérée comme non-écrit et
            cette contradiction n’affecte pas la validité des autres dispositions statutaires.
        </p>
        <p class="font_bold">§29<br>Compétences résiduelles</p>
        <p class="font_regular">Tout ce qui n'est pas prévu explicitement dans les présents statuts est 
            réglé par la loi du 27 juin 1921 régissantles associations sans but lucratif
        </p>
        <p class="font_bold">§30<br>Dispositions transitoires</p>
        <p class="font_regular">Dispositions transitoires. Fait à Bruxelles le 31/05/2018 en 3 exemplaires.
